package com.corejava.character;

public class CharacterEscapeSequences {

	public static void main(String[] args) {
		System.out.println("Saying \'Hello\' to you!!!");
		System.out.println("Saying \"Hello\" to you!!!");
		System.out.println("Saying \nHello to you!!!");
		System.out.println("Saying \bHello to you!!!");
		System.out.println("Saying \tHello to you!!!");
	}

}
